<?php
  class User {
    private $db;

    public function __construct(){
      $this->db = new Database;
    }

    // Regsiter user
    public function register($data){
      $this->db->query('INSERT INTO PIZZA_YOU_users (username, email, password, user_type) VALUES(:username, :email, :password, :user_type)');
      // Bind values
      $this->db->bind(':username', $data['uname']);
      $this->db->bind(':email', $data['email']);
      $this->db->bind(':password', $data['pwd']);
      $this->db->bind(':user_type', $data['user_type']);

      // Execute
      if($this->db->execute()){
        return true;
      } else {
        return false;
      }
    }

    // Login User
    public function login($email, $password){
      $this->db->query('SELECT * FROM PIZZA_YOU_users WHERE email = :email');
      $this->db->bind(':email', $email);

      $row = $this->db->single();

      $hashed_password = $row->password;
      if(sha1($password) == $hashed_password){
        return $row;
      } else {
        return false;
      }
    }

    // Find user by email
    public function findUserByEmail($email){
      $this->db->query('SELECT * FROM PIZZA_YOU_users WHERE email = :email');
      // Bind value
      $this->db->bind(':email', $email);

      $row = $this->db->single();

      // Check row
      if($this->db->rowCount() > 0){
        return true;
      } else {
        return false;
      }
    }
  }